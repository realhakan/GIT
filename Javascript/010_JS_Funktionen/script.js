function add(a, b) {
    return a + b;
  }
  
  // Funktion zum Subtrahieren von zwei Zahlen
  function subtract(a, b) {
    return a - b;
  }
  
  // Funktion zum Multiplizieren von zwei Zahlen
  function multiply(a, b) {
    return a * b;
  }
  
